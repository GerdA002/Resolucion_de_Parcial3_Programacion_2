package org.parcial3.modelo;

public class Cliente {
    private String nombre;
    private String nrc;

    public Cliente() {
    }

    public String getNombre() {
        return this.nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNrc() {
        return this.nrc;
    }

    public void setNrc(String nrc) {
        this.nrc = nrc;
    }
}
